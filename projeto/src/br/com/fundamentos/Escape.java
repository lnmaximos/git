package br.com.fundamentos;

public class Escape {
	
	public static void main(String[] args) {
		System.out.println("Usando o \"eclipse\"");
		System.out.println("Usando o \neclipse");
		System.out.println("Usando o \b eclipse");
		System.out.println("Usando o \t eclipse");
		System.out.println("Usando o \f eclipse");
		System.out.println("Usando o \r eclipse");
		System.out.println("Usando o \' eclipse");
		System.out.println("Usando o \\ eclipse");
		
		/*
		 *  COMENTÁRIO COM MULTIPLAS LINHAS
		 *  1
		 *  2
		 *  3
		 *  4
		 *  5
		 *  6
		 *  7
		 */
	}
}